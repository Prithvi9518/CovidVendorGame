
package Programming_Project;


public class UIButton {
	//Fields
	private double posX;
	private double posY;
	
	//Constructor
	public UIButton(double posX, double posY) {
		this.posX = posX;
		this.posY = posY;
	}
	
	//Getters
	public double getPosX() {
		return posX;
	}

	public double getPosY() {
		return posY;
	}
	
	//Setters
	public void setPosX(double posX) {
		this.posX = posX;
	}

	public void setPosY(double posY) {
		this.posY = posY;
	}
	
	
	
	
}
